 #include <stdio.h>

int main() {

  char minhaLetra = 'L';

  printf("Letra: %c\n",minhaLetra); 

  printf("Letra como inteiro: %d\n",minhaLetra);

  printf("Letra como ponto flutuante: %f\n",minhaLetra);

  int meuInteiro = minhaLetra;

  printf("Inteiro: %d\n",minhaLetra);

  system("pause");

  return 0;

}

